void push(){
     int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    sort(a.begin(), a.end());
    int sum = a[0];
    for (int i = 1; i < n; i++) {
        if (sum + a[i] >= sum * a[i]) sum += a[i]; 
        else sum *= a[i];
        sum %= MOD;
    }
    cout << sum << endl;
}

